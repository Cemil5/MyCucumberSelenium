package day05_unary_relationalOperators;

public class AssignmentOperators {
	public static void main(String[] args) {
		
		int i = 100;
		System.out.println(i);
		
		i += 1;
		System.out.println(i);
		
		i -= 1;
		System.out.println(i);
		
		i /= 4;
		System.out.println(i);
		
		i %= 10;
		System.out.println(i);
		
	}

}
