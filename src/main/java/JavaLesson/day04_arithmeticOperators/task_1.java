package day04_arithmeticOperators;

public class task_1 {
	public static void main(String[] args) {
		int outcome = 12+6/3;
		System.out.println(outcome);
		
		outcome= (5+2)*4;
		System.out.println(outcome);

		outcome= 10/(5-3);
		System.out.println(outcome);
		
		outcome= 8 + 12*(6 - 2);
		System.out.println(outcome);
		
		outcome= 9 % 2;
		System.out.println(outcome);
		
		outcome= (6 - 3) * (2 +7) / 3;
		System.out.println(outcome);
		

	}

}
